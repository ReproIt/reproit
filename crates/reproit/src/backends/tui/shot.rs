/// Glyph bitmap for a char, or None if outside the baked ASCII range (the cell
/// then renders as background only, which is fine for box-drawing / non-ASCII).
fn glyph(c: char) -> Option<&'static [u8; 16]> {
    let code = c as u32;
    if (0x20..=0x7E).contains(&code) {
        Some(&FONT_8X16[(code - 0x20) as usize])
    } else {
        None
    }
}

/// The standard xterm 256-color palette as RGB. Index 0..16 are the classic
/// ANSI colors, 16..232 a 6x6x6 cube, 232..256 a grayscale ramp. This is the
/// same mapping every terminal uses, so an `Idx` color renders faithfully.
fn idx_rgb(i: u8) -> [u8; 3] {
    match i {
        // 16 base ANSI colors (a common xterm rendering).
        0 => [0x00, 0x00, 0x00],
        1 => [0xcd, 0x00, 0x00],
        2 => [0x00, 0xcd, 0x00],
        3 => [0xcd, 0xcd, 0x00],
        4 => [0x00, 0x00, 0xee],
        5 => [0xcd, 0x00, 0xcd],
        6 => [0x00, 0xcd, 0xcd],
        7 => [0xe5, 0xe5, 0xe5],
        8 => [0x7f, 0x7f, 0x7f],
        9 => [0xff, 0x00, 0x00],
        10 => [0x00, 0xff, 0x00],
        11 => [0xff, 0xff, 0x00],
        12 => [0x5c, 0x5c, 0xff],
        13 => [0xff, 0x00, 0xff],
        14 => [0x00, 0xff, 0xff],
        15 => [0xff, 0xff, 0xff],
        16..=231 => {
            // 6x6x6 color cube; each axis steps 0,95,135,175,215,255.
            let n = i - 16;
            let steps = [0u8, 95, 135, 175, 215, 255];
            let r = steps[(n / 36) as usize];
            let g = steps[((n / 6) % 6) as usize];
            let b = steps[(n % 6) as usize];
            [r, g, b]
        }
        232..=255 => {
            // 24-step grayscale ramp from 8 to 238.
            let v = 8 + (i - 232) * 10;
            [v, v, v]
        }
    }
}

/// The default foreground (light gray on a dark terminal) and background (near
/// black). Matches the look of a typical terminal so a `Default`-colored screen
/// reads as white-on-black rather than a flat block.
const DEFAULT_FG: [u8; 3] = [0xc0, 0xc0, 0xc0];
const DEFAULT_BG: [u8; 3] = [0x0c, 0x0c, 0x0c];

fn resolve(color: Color, default: [u8; 3]) -> [u8; 3] {
    match color {
        Color::Default => default,
        Color::Idx(i) => idx_rgb(i),
        Color::Rgb(r, g, b) => [r, g, b],
    }
}

/// Render the current vt100 screen to an RGB image: one CELL_W x CELL_H block
/// per cell, filled with the cell's background color, glyph painted in the
/// foreground color. `inverse` cells swap fg/bg (so a highlighted/selected row
/// reads right). Deterministic: identical grid + colors always produce
/// identical pixels.
pub fn render_screen(parser: &Arc<Mutex<vt100::Parser>>) -> image::RgbImage {
    let p = parser.lock().unwrap();
    let screen = p.screen();
    let (rows, cols) = screen.size();
    let mut img = image::RgbImage::new(cols as u32 * CELL_W, rows as u32 * CELL_H);
    for row in 0..rows {
        for col in 0..cols {
            let (ch, fg, bg) = match screen.cell(row, col) {
                Some(cell) => {
                    let s = cell.contents();
                    let ch = s.chars().next().unwrap_or(' ');
                    let fg = resolve(cell.fgcolor(), DEFAULT_FG);
                    let bg = resolve(cell.bgcolor(), DEFAULT_BG);
                    if cell.inverse() {
                        (ch, bg, fg)
                    } else {
                        (ch, fg, bg)
                    }
                }
                None => (' ', DEFAULT_FG, DEFAULT_BG),
            };
            // A cell whose fg == bg with a glyph would be invisible; that is the
            // app's choice (e.g. a hidden password), so we honor it as-is.
            let x0 = col as u32 * CELL_W;
            let y0 = row as u32 * CELL_H;
            // Fill the cell background.
            for dy in 0..CELL_H {
                for dx in 0..CELL_W {
                    img.put_pixel(x0 + dx, y0 + dy, image::Rgb(bg));
                }
            }
            // Paint the glyph foreground pixels.
            if let Some(bitmap) = glyph(ch) {
                for (dy, byte) in bitmap.iter().enumerate() {
                    for dx in 0..CELL_W {
                        if byte & (0x80 >> dx) != 0 {
                            img.put_pixel(x0 + dx, y0 + dy as u32, image::Rgb(fg));
                        }
                    }
                }
            }
        }
    }
    img
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn idx_palette_anchors() {
        assert_eq!(idx_rgb(0), [0, 0, 0], "ansi black");
        assert_eq!(idx_rgb(15), [0xff, 0xff, 0xff], "ansi bright white");
        assert_eq!(idx_rgb(16), [0, 0, 0], "cube origin is black");
        assert_eq!(idx_rgb(231), [255, 255, 255], "cube far corner is white");
        assert_eq!(idx_rgb(232), [8, 8, 8], "grayscale ramp start");
    }

    #[test]
    fn renders_known_grid_dimensions_and_pixels() {
        // Feed a tiny known screen: 'A' in red-on-black, rest blank, into a 4x2
        // vt100 grid, then assert the image size and a few pixels.
        let parser = Arc::new(Mutex::new(vt100::Parser::new(2, 4, 0)));
        // SGR 31 = red fg; write 'A' at top-left, leave the rest default.
        parser.lock().unwrap().process(b"\x1b[31mA");
        let img = render_screen(&parser);
        assert_eq!(img.width(), 4 * CELL_W, "4 cols * 8px");
        assert_eq!(img.height(), 2 * CELL_H, "2 rows * 16px");

        // The 'A' glyph (0x41) has set pixels; its background is DEFAULT_BG and
        // every set glyph pixel must be ANSI red (idx 1). Find a lit pixel.
        let red = image::Rgb(idx_rgb(1));
        let bg = image::Rgb(DEFAULT_BG);
        let bitmap = glyph('A').unwrap();
        // locate the first set bit in the glyph and check that pixel is red.
        let mut checked_red = false;
        'outer: for (dy, byte) in bitmap.iter().enumerate() {
            for dx in 0..CELL_W {
                if byte & (0x80 >> dx) != 0 {
                    assert_eq!(*img.get_pixel(dx, dy as u32), red, "glyph pixel is fg red");
                    checked_red = true;
                    break 'outer;
                }
            }
        }
        assert!(checked_red, "the 'A' glyph must have a lit pixel");
        // A blank cell (col 3, row 1) is pure background.
        assert_eq!(
            *img.get_pixel(3 * CELL_W, CELL_H),
            bg,
            "empty cell is background"
        );
        // Corner of the 'A' cell (top-left pixel) is background, not glyph (the
        // font's top row is blank for 'A').
        assert_eq!(*img.get_pixel(0, 0), bg, "A's top-left pixel is background");
    }

    #[test]
    fn unknown_glyph_falls_back_to_blank_cell() {
        // A box-drawing char has no baked glyph: the cell renders as background.
        let parser = Arc::new(Mutex::new(vt100::Parser::new(1, 1, 0)));
        parser.lock().unwrap().process("\u{2500}".as_bytes()); // ─
        let img = render_screen(&parser);
        let bg = image::Rgb(DEFAULT_BG);
        for y in 0..CELL_H {
            for x in 0..CELL_W {
                assert_eq!(*img.get_pixel(x, y), bg, "no-glyph cell is all background");
            }
        }
    }
}
