//! Screenshot capture for the TUI backend. Renders the current vt100 cell grid
//! to a PNG store/doc image. Deterministic from the grid (same screen + colors
//! -> same pixels), dependency-light (the `image` crate plus a baked 8x16 ASCII
//! bitmap font, no runtime font rasterizer).
//!
//! Contract (see backends/drive.rs): on a named shoot the runner writes
//! `$REPROIT_SHOTS_DIR/<name>.png` then prints `SHOOT:<name>`. The orchestrator
//! confirms the file exists (RunnerSide capture). If REPROIT_SHOTS_DIR is unset
//! we skip the PNG but still print the marker.

